package Arogyahospital;

public class Supplier {
	int SupplierID; 
	String SupplierName;
	String DateAndTimeofSupply;
	public int getSupplierID() {
		return SupplierID;
	}
	public void setSupplierID(int supplierID) {
		SupplierID = supplierID;
	}
	public String getSupplierName() {
		return SupplierName;
	}
	public void setSupplierName(String supplierName) {
		SupplierName = supplierName;
	}
	public String getDateAndTimeofSupply() {
		return DateAndTimeofSupply;
	}
	public void setDateAndTimeofSupply(String dateAndTimeofSupply) {
		DateAndTimeofSupply = dateAndTimeofSupply;
	}

}
